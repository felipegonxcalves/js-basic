var myValue = 5;

myValue = myValue - 1;

// A expressão é executada e o valor é decrementado depois.
myValue--;

// O valor é decrementado e a expressão é executada depois.
--myValue;

myValue++;

++myValue;

// Cada linha de comando é chamada de Expressão

